package com.vstand4u;

public class DefaultValueDemo {
	byte value1;
	short value2;
	int value3;
	long value4;
	float value5;
	double value6;
	char value7;
	String value8;

	public static void main(String[] args) {
		DefaultValueDemo d=new DefaultValueDemo();
		System.out.println(d.value1);
		System.out.println(d.value2);
		System.out.println(d.value3);
		System.out.println(d.value4);
		System.out.println(d.value5);
		System.out.println(d.value6);
		System.out.println(d.value7);
		System.out.println(d.value8);
	}

}
